package com.mdcbeta.data.remote.model;

public class ForgotPass {
  public String id;

  public String email;




  public ForgotPass() {
  }



  public String getId() {
    return id;
  }

  public void setId(String id) {
    this.id = id;
  }





  public String getEmail() {
    return email;
  }

  public void setEmail(String email) {
    this.email = email;
  }






  public ForgotPass(String id,String email) {
    this.id = id;
    this.email = email;


  }



}



